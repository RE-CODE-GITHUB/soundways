<h2>API</h2>

<p>Clarity provides a simple API that you can use to interact with the plugin.</p>

<h4>Events</h4>
<p>You can bind event handlers using the
    built in on and once functions.</p>

<pre>
    <code>
        var clarity = $('#myPlayer').ttwClarityPlayer(myPlaylist, {});

        clarity.on('playlistAdvance', function(){
        //do something in here when the playlist advances
        });
    </code>
</pre>

<p>You can view a list of all events generated by Clarity here <a href="documentation.php?section=api-events">Events</a>
</p>

<h4>Methods</h4>

<p>You can also call functions on Clarity to manipulate it's behavior</p>

<pre>
    <code>
        var clarity = $('#myPlayer').ttwClarityPlayer(myPlaylist, {});

        //advance to the next song
        clarity.next();

        //change the layout
        clarity.setLayout({
            layout:'album-covers',
            styleType:'blur'
        });
    </code>
</pre>

<p>You can view a list of all events generated by Clarity here <a href="documentation.php?section=api-methods">Methods</a>
</p>




